public interface Shape
{
  /**
   * Computes the area of the shape
   * 
   * @return    The computed area
   */
  double getArea();

  /**
   * Computes the perimeter of the shape
   * 
   * @return    The computed perimeter
   */
  double getPerimeter();

  /**
   * Gets the shape's color
   * 
   * @return    The color
   */
  String getColor();
}
